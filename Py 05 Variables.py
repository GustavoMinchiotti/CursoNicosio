# Variables
# Python es orientado a objetos y no es necesario declarar las variables antes de usarla
# Soporta tres clases de numeros: enteros, flotantes y complejos

# definimos un entero. El operador = es de asignacion
valor=5
print (valor)
# Definimos un flotante
precio=18.5
print (precio)

# Lo definimos con constructor
saldo=float(14)
print(saldo)

# Podemos definir directamente al hacer la operacion
total=precio+saldo
print (total)

# Se pueden tener variables de tipo string
nombre = "Aldo"
print (nombre)

